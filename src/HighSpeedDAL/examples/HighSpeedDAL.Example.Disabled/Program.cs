        services.AddSingleton<StoreDatabase>();

        // Source generators will auto-register DAL classes:
        // - ProductDal
        // - CategoryDal  
        // - CustomerDal
        // - OrderDal
        // - OrderItemDal
        // - InventoryTransactionDal
        // All are automatically available for injection
    }

