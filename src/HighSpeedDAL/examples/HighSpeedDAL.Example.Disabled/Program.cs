    private static void ConfigureServices(ServiceCollection services, IConfiguration configuration)
    {
        // Register configuration
        services.AddSingleton(configuration);

        // Register logging
        services.AddLogging(builder =>
        {
            builder.AddConsole();
            builder.AddConfiguration(configuration.GetSection("Logging"));
        });

        // Register database connection factory
        services.AddSingleton<IDbConnectionFactory, SqlServerConnectionFactory>();

        // Register retry policy factory
        services.AddSingleton<RetryPolicyFactory>(sp =>
        {
            ILogger<RetryPolicyFactory> logger = sp.GetRequiredService<ILogger<RetryPolicyFactory>>();
            int maxRetryAttempts = configuration.GetValue<int>("DalSettings:MaxRetryAttempts", 3);
            int retryDelayMs = configuration.GetValue<int>("DalSettings:RetryDelayMilliseconds", 100);

            return new RetryPolicyFactory(logger, maxRetryAttempts, retryDelayMs);
        });

