    /// <summary>
    /// Demonstrates caching strategies
    /// </summary>
    private static async Task DemonstrateCaching(ServiceProvider serviceProvider, ILogger logger)
    {
        logger.LogInformation("=== Demonstrating Caching Strategies ===");

        // ProductDal productDal = serviceProvider.GetRequiredService<ProductDal>();

        logger.LogInformation("Product uses TwoLayer caching strategy");
        logger.LogInformation("First read will hit the database...");
        DateTime start = DateTime.UtcNow;
        // Product? product = await productDal.GetByIdAsync(1);
        TimeSpan firstRead = DateTime.UtcNow - start;
        logger.LogInformation("First read: {Elapsed}ms (database)", firstRead.TotalMilliseconds);

        logger.LogInformation("Second read will hit L2 cache (ConcurrentDictionary)...");
        start = DateTime.UtcNow;
        // product = await productDal.GetByIdAsync(1);
        TimeSpan secondRead = DateTime.UtcNow - start;
        logger.LogInformation("Second read: {Elapsed}ms (L2 cache)", secondRead.TotalMilliseconds);

        logger.LogInformation("Waiting for cache promotion to L1...");
        await Task.Delay(6000); // Wait for promotion timer (5 second default)

        logger.LogInformation("Third read will hit L1 cache (lock-free Dictionary)...");
        start = DateTime.UtcNow;
        // product = await productDal.GetByIdAsync(1);
        TimeSpan thirdRead = DateTime.UtcNow - start;
        logger.LogInformation("Third read: {Elapsed}ms (L1 cache - ultra-fast)", thirdRead.TotalMilliseconds);

        logger.LogInformation("Cache speedup: ~{Speedup}x faster than database", 
            (int)(firstRead.TotalMilliseconds / Math.Max(thirdRead.TotalMilliseconds, 0.001)));

        logger.LogInformation("Caching demonstration complete\n");
    }

