        // Also test string-based WHERE clause
        sw.Restart();
        var results2 = table.Select("Price > 50 AND StockQuantity > 100").ToList();
        sw.Stop();

        logger.LogInformation("  [SELECT] String WHERE clause: {Elapsed:F2}ms - {Results} matches",
            sw.Elapsed.TotalMilliseconds, results2.Count);
    }

